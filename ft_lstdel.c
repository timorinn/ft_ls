#include "ft_ls.h"
#include <stdlib.h>

int		ft_lstdel(t_list **l)
{
	if (l && *l)
	{
		if ((*l)->next)
			ft_lstdel(&((*l)->next));
		ft_strdel(&((*l)->name));
		free(*l);
		*l = NULL;
	}
	return (0);
}
